class Solution {
  public:

    // Function to return length of longest subsequence of consecutive integers.
    int longestConsecutive(vector<int>& arr) {
        // Your code here
        unordered_set<int> s;
        int ans = 0;
        
        for(int i: arr) s.insert(i);
        for(int i: arr) {
            if(s.find(i - 1) != s.end()) continue;
            else {
                int j = i;
                while(s.find(j) != s.end()) j++;
                ans = max(ans, j-i);
            }
        }
        
        return ans;
    }
};

